-- --------------------------------------------------------
-- 原子检查并自增 OCR 用量的函数 (check_and_increment_ocr_usage)
-- 返回: { allowed: boolean, current_count: int, max_limit: int }
-- --------------------------------------------------------
CREATE OR REPLACE FUNCTION public.check_and_increment_ocr_usage(
  p_max_limit int default 10
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS
$$
declare
  v_user_id uuid := auth.uid();
  v_today date := current_date;
  v_current_count int;
begin
  if v_user_id is null then
    return jsonb_build_object('allowed', false, 'current_count', 0, 'max_limit', p_max_limit, 'error', 'NOT_AUTHENTICATED');
  end if;

  -- 尝试 upsert 并原子自增
  insert into public.ocr_daily_usage (user_id, usage_date, count)
  values (v_user_id, v_today, 1)
  on conflict (user_id, usage_date)
  do update set count = ocr_daily_usage.count + 1
  where ocr_daily_usage.count < p_max_limit
  returning count into v_current_count;

  -- 如果没有 returning 结果，说明已达上限（WHERE 条件不满足）
  if v_current_count is null then
    select count into v_current_count
    from public.ocr_daily_usage
    where user_id = v_user_id and usage_date = v_today;

    return jsonb_build_object('allowed', false, 'current_count', coalesce(v_current_count, 0), 'max_limit', p_max_limit);
  end if;

  return jsonb_build_object('allowed', true, 'current_count', v_current_count, 'max_limit', p_max_limit);
end;
$$;

GRANT EXECUTE ON FUNCTION public.check_and_increment_ocr_usage(int) TO authenticated;
GRANT EXECUTE ON FUNCTION public.check_and_increment_ocr_usage(int) TO service_role;
